#include<unistd.h>

int	ft_str_is_alpha(char	*str)
{
	int	i;
	int	r;

	r = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z')
			|| (str[i] >= 'A' && str[i] <= 'Z'))
		{
			r = 1;
			i++;
		}
		else
		{
			r = 0;
			break ;
		}
	}
	return (r);
}
