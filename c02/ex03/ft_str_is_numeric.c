#include<unistd.h>

int	ft_str_is_numeric(char	*str)
{
	int	i;
	int	r;

	r = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			r = 1;
			i++;
		}
		else
		{
			r = 0;
			break ;
		}
	}
	return (r);
}
